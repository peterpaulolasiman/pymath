'''
A module that emulates the functionality
of add, Subtrackt, divide and multiply
'''


def add(x, y):
    """
    This function takes in x and y
    and adds them.
    :param x: any number
    :param y: any number
    :return: the sum of x and y
    """

    return x + y

def subtract(x, y):
    """
    This function takes in x and y
    and subtract them.
    :param x: any number
    :param y: any number
    :return: the difference of x from  y
    """

    return x-y

def multiply(x, y):
    """
    This function takes in x and y
    and multiply them.
    :param x: any number
    :param y: any number
    :return: the product of x and y
    """

    return x * y

def divide(x, y):
    """
    This function takes in x and y
    and divide them.
    :param x: any number
    :param y: any number
    :return: the division of x  by y
    """

    return x / y



